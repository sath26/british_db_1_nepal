DROP SEQUENCE STAGING_PROJECT_SEQ
CREATE SEQUENCE   "STAGING_PROJECT_SEQ" 
 MINVALUE 1
 MAXVALUE 9999999999 
 INCREMENT BY 1 
 START WITH 1 
 CACHE 20 
 NOORDER  
 NOCYCLE ;

CREATE OR REPLACE TRIGGER  "STAGING_PROJECT_TRIGGER" 
before insert on STG_PROJECT
for each row
begin
:new.PROJECT_KEY:=STAGING_PROJECT_SEQ.NEXTVAL;
end;


