	"PLCT_ESTIMATED_END_DATE" DATE, 
	"PLCT_ACTUAL_START_DATE" DATE, 
	"PLCT_ACTUAL_END_DATE" DATE, 
	"PLCT_RENEWAL_NUMBER" NUMBER, 
	"PLCT_RATE_DAY_PROJ" VARCHAR2(50), 
	"FEEDBACK_VALUE" NUMBER, 
	"ACTUAL_SALARY" NUMBER, 
	"CONSULTANT_ID" NUMBER, 
	"ACCOUNT_ID" NUMBER,
